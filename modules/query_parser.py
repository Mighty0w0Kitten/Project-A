import re

# Функция для анализа запросов
def parse_query(query):
    """
    Эта функция анализирует запрос и возвращает команду, которую нужно выполнить.
    """
    query = query.lower()  # Преобразуем запрос в нижний регистр для унификации

    # Пример простого анализа: ищем ключевые слова в запросе
    if re.search(r"\bпривет\b", query):
        return "Привет! Как я могу помочь?"
    elif re.search(r"\bпока\b", query):
        return "До свидания!"
    elif re.search(r"\bпогода\b", query):
        return "Давай проверим погоду."
    else:
        return "Извините, я не понимаю ваш запрос."
